/**
 * scripts/gen-brand-icons.cjs
 *
 * Generates src/prototype/brandIcons.tsx from the vendored `simple-icons`
 * package: inlined SVG path data, NO runtime CDN request, fully self-contained
 * (works under strict CSP / offline). Re-run after editing MAP:
 *   node scripts/gen-brand-icons.cjs
 */
const fs = require('fs');
const path = require('path');
const si = require('simple-icons');

// app id (lowercase, as used across the SaaS + Fuentes provider names) -> simple-icons slug
const MAP = {
  zendesk:'zendesk', intercom:'intercom', freshdesk:'freshworks', helpscout:'helpscout', front:'front',
  shopify:'shopify', woocommerce:'woocommerce', bigcommerce:'bigcommerce', stripe:'stripe', paypal:'paypal', adyen:'adyen',
  slack:'slack', teams:'microsoftteams', whatsapp:'whatsapp', messenger:'messenger', instagram:'instagram', telegram:'telegram',
  gmail:'gmail', twilio:'twilio', aircall:'aircall', mailchimp:'mailchimp', klaviyo:'klaviyo', discord:'discord',
  ups:'ups', dhl:'dhl', hubspot:'hubspot', salesforce:'salesforce', pipedrive:'pipedrive', docusign:'docusign',
  notion:'notion', gdrive:'googledrive', confluence:'confluence', jira:'jira', linear:'linear', github:'github', gitlab:'gitlab',
  asana:'asana', calendly:'calendly', gcalendar:'googlecalendar', zoom:'zoom', sentry:'sentry',
  quickbooks:'quickbooks', plaid:'plaid', segment:'segment', zapier:'zapier',
  anthropic:'anthropic', openai:'openai', ollama:'ollama', gemini:'googlegemini',
  guru:'guru', getguru:'guru', outlook:'microsoftoutlook',
};

function lookup(slug) {
  if (typeof si.get === 'function') { try { const r = si.get(slug); if (r) return r; } catch {} }
  const pascal = 'si' + slug.split(/[^a-z0-9]/i).map(s => s.charAt(0).toUpperCase() + s.slice(1)).join('');
  return si[pascal] || null;
}

const out = {};
const missing = [];
for (const [id, slug] of Object.entries(MAP)) {
  const icon = lookup(slug);
  if (!icon || !icon.path) { missing.push(`${id} (${slug})`); continue; }
  out[id] = { title: icon.title, hex: icon.hex, path: icon.path };
}

// Merge brands removed from current simple-icons (trademark takedowns) that we
// vendored from an older release into scripts/brand-icons-extra.json.
try {
  const extra = JSON.parse(fs.readFileSync(path.resolve(__dirname, 'brand-icons-extra.json'), 'utf8'));
  for (const [id, spec] of Object.entries(extra)) {
    if (!out[id] && spec && spec.path) {
      out[id] = { title: spec.title, hex: spec.hex, path: spec.path };
      const i = missing.indexOf(missing.find(m => m.startsWith(id + ' ')) || '');
      if (i >= 0) missing.splice(i, 1);
    }
  }
} catch { /* extras optional */ }

const body = `// ────────────────────────────────────────────────────────────────────────────────
// Brand icons — AUTO-GENERATED by scripts/gen-brand-icons.cjs from the vendored
// \`simple-icons\` package. Inlined SVG path data: NO runtime CDN request, fully
// self-contained (works under strict CSP / offline). Re-generate after adding a
// brand to MAP in the generator. Do not hand-edit the BRAND_ICONS data below.
//
// Brands: ${Object.keys(out).length}. Missing from simple-icons: ${missing.length ? missing.join(', ') : 'none'}.
// ────────────────────────────────────────────────────────────────────────────────
import type { CSSProperties } from 'react';

export interface BrandIconSpec { title: string; hex: string; path: string; }

export const BRAND_ICONS: Record<string, BrandIconSpec> = ${JSON.stringify(out, null, 2)};

// Aliases so lookups by display name / synonym resolve to the same glyph.
const ALIASES: Record<string, string> = {
  'google drive': 'gdrive', 'google calendar': 'gcalendar', 'ms teams': 'teams',
  'microsoft teams': 'teams', 'get guru': 'guru',
};

export function resolveBrandId(name: string | undefined | null): string | null {
  if (!name) return null;
  const k = String(name).trim().toLowerCase();
  if (BRAND_ICONS[k]) return k;
  if (ALIASES[k] && BRAND_ICONS[ALIASES[k]]) return ALIASES[k];
  const compact = k.replace(/[^a-z0-9]/g, '');
  if (BRAND_ICONS[compact]) return compact;
  return null;
}

// Brand colours for apps whose official glyph isn't in simple-icons, so the
// lettered fallback tile still reads as that brand instead of a neutral grey.
const FALLBACK_COLORS: Record<string, string> = {
  guru: '#FF5C5C', getguru: '#FF5C5C', freshdesk: '#25C16F', freshworks: '#FF5A1F',
  box: '#0061D5', document360: '#EC1944', front: '#A857F0', klaviyo: '#111111',
  pipedrive: '#017737', plaid: '#111111', segment: '#52BD95',
};

export interface BrandIconProps {
  /** App id or brand display name (e.g. 'intercom', 'Zendesk', 'Google Drive'). */
  name: string;
  size?: number;
  /** Override the fill. Defaults to the official brand colour. */
  color?: string;
  /** Render the mark in currentColor (e.g. white on a brand-colored tile). */
  monochrome?: boolean;
  className?: string;
  style?: CSSProperties;
  title?: string;
}

/**
 * Inline SVG brand glyph. Renders the official single-path mark in a 24×24
 * viewBox. Falls back to a neutral lettered tile when the brand is unknown.
 */
export function BrandIcon({ name, size = 20, color, monochrome = false, className = '', style, title }: BrandIconProps) {
  const id = resolveBrandId(name);
  const spec = id ? BRAND_ICONS[id] : null;
  if (!spec) {
    const letter = (String(name || '?').trim()[0] || '?').toUpperCase();
    const key = String(name || '').trim().toLowerCase().replace(/[^a-z0-9]/g, '');
    const bg = FALLBACK_COLORS[key] ?? '#646462';
    return (
      <span
        className={className}
        style={{ width: size, height: size, borderRadius: size * 0.22, background: bg, color: '#fff', display: 'inline-flex', alignItems: 'center', justifyContent: 'center', fontSize: size * 0.5, fontWeight: 700, ...style }}
        aria-label={title || String(name)}
      >
        {letter}
      </span>
    );
  }
  const fill = color ?? (monochrome ? 'currentColor' : \`#\${spec.hex}\`);
  return (
    <svg viewBox="0 0 24 24" width={size} height={size} className={className} style={style} role="img" aria-label={title || spec.title} fill={fill}>
      <path d={spec.path} />
    </svg>
  );
}

/** Official brand colour hex (with #) for a brand, or a neutral default. */
export function brandColor(name: string, fallback = '#646462'): string {
  const id = resolveBrandId(name);
  return id ? \`#\${BRAND_ICONS[id].hex}\` : fallback;
}
`;

const dest = path.resolve(__dirname, '../src/prototype/brandIcons.tsx');
fs.writeFileSync(dest, body, 'utf8');
console.log('Wrote', dest);
console.log('Brands:', Object.keys(out).length, '| Missing:', missing.join(', ') || 'none');
